#include <bits/stdc++.h>
#define int long long
#define pb push_back
using namespace std;
const int INF = 1e18;
 
void kekeke() {
	int n,k;
	cin >>n>>k;
	vector<int>lol(k) , tete(k);
	for (int i = 0; i < k; i++) cin >> lol[i];
	for (int i = 0; i < k; i++) cin >> tete[i];
	vector<int>d1(n+2,INF) , d2(n+2,INF);
	for (int i=0; i<k; i++) {
		d1[lol[i]] = tete[i] - lol[i];
		d2[lol[i]] = tete[i] + lol[i];
	}
	for (int i = n ; i>0; i--) {
		d2[i] = min(d2[i],d2[i+1]);
	}
	for (int k=1; k<n+1; k++) {
		d1[k] = min(d1[k],d1[k-1]);
	}
	for (int j = 1; j < n+1; j++) {
		cout << min(d1[j]+j,d2[j]-j) <<" ";
	}
	cout<<endl;
}
 
signed main() { 
  int t;
  cin >> t;
  while (t--){
    kekeke();  
  }
  return 0; 
}
